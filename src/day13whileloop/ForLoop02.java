package day13whileloop;

public class ForLoop02 {

	public static void main(String[] args) {
		
		
		/*
		 * For loop kullanarak asagýda olusturulan sekli olusturunuz.
		                  * 
		                  ** 
		                  *** 
		                  **** 
		                  *****
		   
		 */
		
	for (int i = 1 ; i <=5 ; i++ ) {   // i==> satýr sayýsýný
		
		for(int k = 1; k<=i ; k++) {      // k ==> yýldýz sayýsýný gösterir
			System.out.print("*");
	
		}
	System.out.println();	
	}
		
		
		
		

	}

}
