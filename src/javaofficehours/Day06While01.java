package javaofficehours;

public class Day06While01 {

	public static void main(String[] args) {
		
		/*
		 * while dongusu ile 1 den 100 e kadar sayýlarýn toplamýný yazdýralým
		 */

		
		int sum = 0;
		
		int i=0;
		
		while(i!=101) {
			
			sum = sum+i;
			i++;
		}
	
	System.out.println("1 ile 100 arasýndaki sayýlarýn toplamý : "+ sum);
	
	
	
	
	
	}

}
