public class Operator01 {

	public static void main(String[] args) {
		
	int i1= 12;
	int i2 = 13;
	int i3 = 14;
	int i4 = 15;
	System.out.println(i1 + i2); // 12 + 13 = 25
	
	System.out.println(i1 + i2*i3); // 12 + 13*14 = 194
		
	System.out.println(i4* (i2 + i1/3));	// 15*(13+12/3) = 195
		
		
		
		
	}

}
